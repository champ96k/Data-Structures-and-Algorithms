#include<bits/stdc++.h>
using namespace std;

bool isPow(long long int n) {
    if(n==0)
    return false;
    
    return ((n&(n-1))==0);
}

int main()
{
    //T for Number of Test Cases
    int T;
    cin>>T;
    while(T--)
    {
        //start here
        long long int n;
        cin>>n;
        if(isPow(n)) {
            cout<<"YES"<<endl;
        } else  {
            cout<<"NO"<<endl;
        }
    }
    return 0;
}
