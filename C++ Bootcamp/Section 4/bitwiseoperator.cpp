#include<bits/stdc++.h>
using namespace std;

int main() {
  
  int ch;
  char ans;
  
  do
    {
      cout<<"\n\t1.AND\n\t2.OR\n\t3.XOP\n\t4.NOT\n\t";
      cout<<"Enter you'r choice: ";
      cin>>ch;
      
      switch(ch) {
        case 1:
                cout<<"Hello1";
                break;
        case 2:
                cout<<"Hello2";
                break;
        case 3:
                cout<<"Hello3";
                break;
        case 4:
                cout<<"Hello4";
                break;
      }
      
      cout<<"\n\tDo You Want to Continue(Y/N): ";
      cin>>ans;
      
    }while(ans=='y' || ans=='Y');
  
  return 0;
}
